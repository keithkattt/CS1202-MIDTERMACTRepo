void SetColorAndBackground(int textColor, int bgColor) {
    HANDLE hConsole = GetStdHandle(STD_OUTPUT_HANDLE);
    SetConsoleTextAttribute(hConsole, textColor + (bgColor * 16));
}

void ResetColor() {
    SetColorAndBackground(15, 0); // White text, black background
}

void asciiAnimation() {
    system("cls");
    SetColorAndBackground(13, 0); // Magenta text
    const char *frames[] = {  // Use 'const char*' instead of 'char*'
        "(\\_/)",  // Fixed the escape sequence
        "(o.o)",
        "(> <)"
    };


    printf("\n\tWarming up the system...\n");
    for (int i = 0; i < 10; i++) {
        printf("\r\t%s", frames[i % 3]);
        fflush(stdout);
        Sleep(300);
    }
    ResetColor();
    system("cls");
}

void spinningAnimation() {
    system("cls");
    SetColorAndBackground(14, 0); // Yellow text
    char spinner[] = "|/-\\";
    printf("\n\tInitializing...");
    for (int i = 0; i < 15; i++) {
        printf("\r\t%c", spinner[i % 4]);
        fflush(stdout);
        Sleep(200); // Sleep in milliseconds
    }
    ResetColor();
    system("cls");
}

void loadingAnimation() {
    system("cls");
    SetColorAndBackground(11, 0); // Cyan text
    printf("\n\tLoading ASCII Splash...\n\n");
    for (int i = 0; i < 20; i++) {
        printf("#");
        fflush(stdout);
        Sleep(100); // Sleep in milliseconds
    }
    printf(" Done!\n");
    Sleep(500);
    ResetColor();
    system("cls");
}

void decreaseFontSize(){ 
    
    // Simulate pressing Ctrl
   keybd_event(VK_CONTROL, 0, 0, 0);

   // Simulate pressing '-' (minus key)
   keybd_event(VK_OEM_MINUS, 0, 0, 0);

   // Release '-' key
   keybd_event(VK_OEM_MINUS, 0, KEYEVENTF_KEYUP, 0);

   // Release Ctrl key
   keybd_event(VK_CONTROL, 0, KEYEVENTF_KEYUP, 0);

}

void increaseFontSize(){
    // Simulate pressing CTRL
    keybd_event(VK_CONTROL, 0, 0, 0);

    // Simulate pressing '+'
    keybd_event(VK_OEM_PLUS, 0, 0, 0);

    // Release '+'
    keybd_event(VK_OEM_PLUS, 0, KEYEVENTF_KEYUP, 0);

    // Release CTRL
    keybd_event(VK_CONTROL, 0, KEYEVENTF_KEYUP, 0);

}

